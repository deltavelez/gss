UINT8_T lb_ft_bit_in_rawfont(unsigned char *font_buffer, int character, int i, int j, int cols, int rows)
{
  int index, offset;
  //index = character*(cols*2+1) + i*2 + (int)(j/8) + 1  ;  // Ffirst byte is useless
  index = character*(cols*ceil(rows/8.0)+1) + i*ceil(rows/8.0) + (int)(j/8) + 1  ;  // First byte is useless
  offset=j-8*((int)(j/8));
  if(offset<0)
    {
      printf("Offset =%d\r\n",offset);
      exit(1);
    }
  //  printf("index = %i offset = %i ceil=%f\r\n", index,offset,ceil(rows/8));
  return ( (font_buffer[index] >> offset) & 0x01);
}

